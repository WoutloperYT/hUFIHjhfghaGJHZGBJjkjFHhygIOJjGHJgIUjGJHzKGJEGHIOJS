/* RACES PER SEASON (alleen IDs) */

app.get(":year/:class/races", (req, res) => {

  const { year, class: raceClass } = req.params;

  if (!seasonData[year]) {
    return res.status(404).json({ error: "Season not found" });
  }

  if (!seasonData[year][raceClass]) {
    return res.status(404).json({ error: "Class not found" });
  }

  const races = seasonData[year][raceClass].races.map(r => ({ id: r.id }));

  res.json(races);

});


/* RACE DETAILS PER SEASON */

app.get("/:year/:class/races/:id", (req, res) => {

  const { year, class: raceClass, id } = req.params;

  if (!seasonData[year]) {
    return res.status(404).json({ error: "Season not found" });
  }

  if (!seasonData[year][raceClass]) {
    return res.status(404).json({ error: "Class not found" });
  }

  const races = seasonData[year][raceClass].races;

  let race;

  if (id === "current") {

    const today = new Date();

    // filter races die al gereden zijn
    const pastRaces = races.filter(r => new Date(r.date) <= today);

    // laatste gereden race
    race = pastRaces[pastRaces.length - 1];

  } else {

    race = races.find(r => r.id == id);

  }

  if (!race) {
    return res.status(404).json({ error: "Race not found" });
  }

  res.json(race);

});


